    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $data = Modul::latest();

        if ($request->has('kelas'))
        {
            $data->where('kelas', 'rlike', $request->kelas);
        }

        if ($request->has('pelajaran_id'))
        {
            $data->wherePelajaranId($request->pelajaran_id);
        }

        $data->with('pelajaran')->with('guru')->withCount('materis')->withCount('tesses')->withCount('quizes');

        return response([
            'data' => $data->paginate(10)
        ]);
    }

